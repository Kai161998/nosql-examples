package com.oracle.fleet.to;

public class FleetTO {

	// Row columns names
	public static final String VIN_COLUMN = "vin";
	public static final String MAKE_COLUMN = "make";
	public static final String MODEL_COLUMN = "model";
	public static final String YEAR_COLUMN = "year";
	public static final String FUEL_COLUMN = "fuelType";

	private String vin = null;
	private String make = null;
	private String model = null;
	private int year = 0;
	private String fuelType;

	public String getVin() {
		return vin;
	}

	public void setVin(String vin) {
		this.vin = vin;
	}

	public String getMake() {
		return make;
	}

	public void setMake(String make) {
		this.make = make;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public String getFuelType() {
		return fuelType;
	}

	public void setFuelType(String fuelType) {
		this.fuelType = fuelType;
	}

	public String toString() {
		String str = "Vin: " + this.vin + " Make: " + make + " Model: " + model
				+ " Year: " + year + " Fuel: " + fuelType;
		return str;
	}
}
